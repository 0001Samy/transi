int		ft_count_if(char **tab, int length, int (*f)(char*))
{
	int i;
	int len;

	i = 0;
	len = 0;
	while (i < length)
	{
		if ((*f)(tab[i]) != 0)
			len++;
		i++;
	}
	return (len);
}
